fun main() {

    // var is used when you want to change the variable value afterwards
    //val is used when the variable has a constant value

    var number:Int =2
    println(number)
    val numberDoublePrecision: Double = 2.45
    println(numberDoublePrecision)
    val numberDecimal : Float = 2.45f
    println(numberDecimal)
    val boolean:Boolean = true
    println(boolean)

    val numberLong: Long = 12L
    println(numberLong)


    //Explicit Defining of type. is shown above.


    //Implicit Type of Defining the Variable.
    var a =2
    println("This is the Implicit type of variable $a")











}